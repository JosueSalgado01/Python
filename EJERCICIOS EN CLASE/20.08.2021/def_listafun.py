def listafun(lista):
    for i in lista:
        print('Hola',i)
    print('\n'*1)
listafun(['Juan'])
listafun(['Juan','Felipe'])
listafun(['Juan','Felipe','Luis'])
listafun(['Juan','Felipe','Luis','Andres'])
